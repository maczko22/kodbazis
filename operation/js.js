//operátorok


// (number, number) -> number
console.log(2 + 3);

// +,-,*,/,%
console.log(2 + 4);
console.log(2 - 4);
console.log(2 * 4);
console.log(2 / 4);
console.log(7 % 3);

// összehasonlító (boolean)
// == (eqaulity)

console.log(2 == 1);
console.log(2 == 2);

// != nem egyenlő
console.log(2 != 2);
console.log(2 != 3);

// === (identity)
console.log(1 === true); //false

// >,<, <=, >=
console.log(3 > 5);


//logikai operátorok

//logikai és
console.log(true && false);
console.log(true && true);

// logikai vagy ||
console.log(true || false);

// negálás
console.log(!true);
console.log(!false);



console.log(2 === 2 && 1 < 5);
console.log(2 === 2 && (1 < 5 || true));

console.log(2 * 3 * 5)


//string operátor

console.log('szia' + ' Daniel');

// conditional
console.log(true ? 'első' : 'második');

// Assigment operátor


var valtozo;
console.log(valtozo);

valtozo = 'szia';
console.log(valtozo);

valtozo = 3;

console.log(valtozo);

// increment, decrement

valtozo++;
valtozo--;

console.log(valtozo);

// +=, -=

var szam = 5;
szam += 10;
szam *= 10;
szam /= 10;
szam %= 10;
console.log(szam);